package printhello;

public class FloatVar {
     public static void main(String args[]) {
    	 
    	 float a=89706.98f;
    	 String b="Hello hai how are you";
    	 double db=91599164;
    	 boolean istrue=true;
    	 char d='S';
    	 
    	 System.out.println("The local value of float is "+a);
    	 System.out.println("The local value of string is "+b);
    	 System.out.println("The local value of double is "+db);
    	 System.out.println("The local value of boolean is "+istrue);
    	 System.out.println("The local value of character is "+d);
     }
}
