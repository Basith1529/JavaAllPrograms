package program;

public class DMSSwitchCase {
	
	public static void main(String[] args) {
		
		int a=30;
		
		switch(a) {
		
		case 10:
			System.out.println("The value of a is 10");
			break;
			
		case 20:
			System.out.println("The value of a is 20");
			break;
			
		case 30:
			System.out.println("The value of a is 30");
			break;
			
		case 40:
			System.out.println("The value of a is 40");
			break;
			
			default:
				System.out.println("The value of a is not in 10,20,30,40");
		}
		
	}

}
