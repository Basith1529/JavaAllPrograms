package printhello;
//Instance Variables are declared in a class and 
//outside of any method,constructor or block.
//Instance variable can be accessed only by creating a objects
//we can also initialize using constructor also
//Type 1(using static)
//variable declaration------Datatype,name,how much memory to allocate
public class InstanceVariable {
	 static String a="hello";//Instance variable
	 static int i;//Instance variable
	public static void main(String args[]) {
		
		System.out.println("the values of strings is:"+a);
		System.out.println("the value of integer is:"+i);
		
	}

}
